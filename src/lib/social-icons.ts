/** Lookup a platform by key */
export function getSocialPlatform(key: string): SocialPlatform | undefined {
  return socialPlatforms.find((p) => p.key === key.toLowerCase())
}

/** Get display label for a platform key */
export function getSocialLabel(key: string): string {
  return getSocialPlatform(key)?.label ?? key.charAt(0).toUpperCase() + key.slice(1)
}
